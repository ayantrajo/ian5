    private void idKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_idKeyPressed
       if(evt.getKeyCode() == KeyEvent.VK_ENTER){
           
           String bcode = id.getText();
           
           try {
               Class.forName("com.mysql.jdbc.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost/sample_dbs", "root", "");
                pat = con.prepareStatement("SELECT * FROM book where id = ?");
                pat.setString(1, bcode);
               rst = pat.executeQuery();
                if(rst.next() == false){
                    JOptionPane.showMessageDialog(this, "book Id not found!");
                    
                }else{
                    String bkname = rst.getString("book");
                    String pr = rst.getString("price");
                    bname.setText(bkname.trim());
                    price.setText(pr.trim());
                   
                }
                
                
                
           } catch (ClassNotFoundException ex) {
               Logger.getLogger(booksale.class.getName()).log(Level.SEVERE, null, ex);
           } catch (SQLException ex) {
               Logger.getLogger(booksale.class.getName()).log(Level.SEVERE, null, ex);
           }
          
       }
    }//GEN-LAST:event_idKeyPressed

    private void quantityStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_quantityStateChanged
        int qty = Integer.parseInt(quantity.getValue().toString());
        int prc = Integer.parseInt(price.getText());
        int tot = qty * prc;
        amount.setText(String.valueOf(tot));
        
    }//GEN-LAST:event_quantityStateChanged

