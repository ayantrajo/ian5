    public void Balance(){     
        int ttl = Integer.parseInt(total.getText());
        int py = Integer.parseInt(pay.getText());
        int bal = py - ttl;
        balance.setText(String.valueOf(bal));  
    }
    
    public void Bill(){
        String ttl = total.getText();
        String py = pay.getText();
        String bal = balance.getText();
        
     DefaultTableModel model = new DefaultTableModel();
      model = (DefaultTableModel)jTable1.getModel();
        
        bill.setText(bill.getText() + "**********************\n");
        bill.setText(bill.getText() + "*      POSBILL       *\n");
        bill.setText(bill.getText() + "**********************\n");
        
        //Heading
         bill.setText(bill.getText() + "Book" + "\t" + "Price" + "\t" + "Amount" + "\n");
         
         for(int i = 0; i < model.getRowCount(); i++){
             String bname =(String)model.getValueAt(i,1);
             String price =(String)model.getValueAt(i,3);
             String amount =(String)model.getValueAt(i,4);
             
         bill.setText(bill.getText() + bname + "\t" + price + "\t" + amount + "\n");
         }
         bill.setText(bill.getText() + "\n");
         
         bill.setText(bill.getText() +  "Total: " + ttl + "\n");
         bill.setText(bill.getText() +  "Pay: " + py + "\n");
         bill.setText(bill.getText() +  "Balance: " + bal + "\n");
         bill.setText(bill.getText() + "\n");
         bill.setText(bill.getText() + "**********************\n");
     
    }
    
